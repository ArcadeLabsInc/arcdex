<?php

namespace App\Http\Controllers;

use App\Models\Nonce;
use App\Models\User;
use Illuminate\Http\Request;
use Illuminate\Validation\ValidationException;
use Inertia\Inertia;

class LoginController extends Controller
{
    // render login page via inertia
    public function index()
    {
        return Inertia::render('Login');
    }

    public function nonce(Request $request)
    {
        // validate request has pubkey and device_name as strings
        try {
            $request->validate([
                'pubkey' => 'required',
                'device_name' => 'required',
            ]);
        } catch (\Exception $e) {
            throw ValidationException::withMessages([
                'pubkey' => 'The pubkey field is required.',
            ]);
        }

        $nonce = bin2hex(random_bytes(32));

        Nonce::create([
            'nonce' => $nonce,
            'pubkey' => $request->pubkey,
            'device_name' => $request->device_name
        ]);

        return [
            'nonce' => $nonce
        ];
    }

    public function apiLogin(Request $request)
    {
        $request->validate([
            'pubkey' => 'required',
            'device_name' => 'required',
            'proof' => 'required',
        ]);

        // Validate the proof was generated by the private key associated with the pubkey
        // TODO

        $pubkey = $request->pubkey;
        $user = User::where('pubkey', $pubkey)->first();
        if (!$user) {
            // Create a user with this pubkey and log them in
            $user = User::create([
                'pubkey' => $pubkey
            ]);
        }

        return $user->createToken($request->device_name)->plainTextToken;
    }
}
